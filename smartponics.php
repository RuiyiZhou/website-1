            <div class="row featurette">
            <div class="col-md-5"> 
             <p>This study intended to determine how effectively the SmartPonics application’s design could guide users through the application and hardware setup process, check supply levels and manipulate settings.  We conducted user testing between Nov 17 – Nov 19.</p>
             <p>Our goals were to observe seamless navigation and setup by users with minimal confusion. We tested 10 participants. Our methodology was as follows:</p>
             <p>We began the study by sending users a confirmation email that their SmartPonics account had been created and their apartment manager had assigned them three hydroponic boxes. This “Welcome” email set the context for the user, eliciting the expectation that they would be setting up the application during the test. We had users complete four tasks, described below, and took notes on whether their experience was as fluid as we hoped, noting where user doubt and misdirection occurred.</p>   
